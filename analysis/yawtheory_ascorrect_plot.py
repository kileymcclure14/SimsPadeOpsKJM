CT_Cor_MS_RP = np.load('CT_Cor_MS_RP.npy')
CT_Cor_Wer_RP = np.load('CT_Cor_Wer_RP.npy')

plt.figure(figsize=(10, 6))
plt.plot(C_CTP_Sol, C_CP_RP, label='Beta = 0.35 Data', color = 'blue', marker='o', linestyle='-')
plt.plot(U_CTP_Sol, U_CP_RP, label = 'Beta = 0.005 Data', color = 'black', marker = 'o', linestyle = '-')
plt.plot(C_CTP_Sol,CP_RP_Cor, label= 'Yaw Model Correction', color = 'red', marker='^', linestyle=':')
plt.plot(C_CTP_Sol, CP_Cor_BW_RP, label='Barnsley/Wellicome Correction', color = 'orange', marker = '^', linestyle = ':')
plt.plot(C_CTP_Sol, CP_Cor_Houl_RP, label = 'Houlsby Correction', color = 'yellow', marker = '^', linestyle = ':')
plt.plot(C_CTP_Sol, CP_Cor_Mask_RP, label = 'Maskell Correction', color = 'green', marker = '^', linestyle = ':')
plt.plot(C_CTP_Sol, CP_Cor_MS_RP, label = 'Mikkelsen/Sorensen Correction (Unverified)', color = 'purple', marker = '^', linestyle = ':')
plt.plot(C_CTP_Sol, CP_Cor_Wer_RP, label = 'Werle Correction', color = 'brown', marker = '^', linestyle = ':')
plt.xlabel('C_T Prime')
plt.ylabel('C_P')
plt.title('Blockage Correction Comparison for Beta = 0.35 to Beta = 0.005 using Experimental Data')
plt.legend(bbox_to_anchor=(1.05, 1), loc='upper left')
plt.grid()
plt.savefig('yawtheory_ascorrect_plot.png', bbox_inches='tight', dpi=300)

