import numpy as np
import matplotlib.pyplot as plt

C_CP_RP = np.load('C_CP_RP.npy')
C_CTP_Sol = np.load('C_CTP_Sol.npy')
CP_Cor_BW_RP = np.load('CP_Cor_BW_RP.npy')
CP_Cor_Houl_RP = np.load('CP_Cor_Houl_RP.npy')
CP_Cor_Mask_RP = np.load('CP_Cor_Mask_RP.npy')
